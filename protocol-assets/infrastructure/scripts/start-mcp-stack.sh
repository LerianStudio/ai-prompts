    echo
    log_success "ðŸš€ Lerian Protocol MCP Stack Started Successfully!"
    echo
    echo "   ${BOLD}Configuration:${NC}"
    echo "   â”œâ”€â”€ Mode: $MODE"
    echo "   â”œâ”€â”€ Board Service: http://$HOST:$SERVICE_PORT"
    if [ "$MODE" != "dev" ]; then
        echo "   â”œâ”€â”€ MCP Interface: Configured and ready"
    fi
    echo "   â””â”€â”€ Database: $DB_PATH"
    echo
    echo "   ${BOLD}Useful Commands:${NC}"
    echo "   â”œâ”€â”€ Health Check: curl http://$HOST:$SERVICE_PORT/health"
    echo "   â”œâ”€â”€ List Tasks: curl http://$HOST:$SERVICE_PORT/api/tasks"
    echo "   â”œâ”€â”€ Stack Status: ./scripts/mcp-stack-status.sh"
    echo "   â””â”€â”€ Stop Stack: ./scripts/stop-mcp-stack.sh"
    echo
    if [ "$MODE" != "dev" ]; then
        echo "   ${BOLD}Logs:${NC}"
